package com.fanfull.entity;

public class ScanBean {
	private String rfidString;
	private String typeString;
	private String bagbarcode;
	private String barcode;
	private String bagid;
	public String getRfidString() {
		return rfidString;
	}
	public void setRfidString(String rfidString) {
		this.rfidString = rfidString;
	}
	public String getTypeString() {
		return typeString;
	}
	public void setTypeString(String typeString) {
		this.typeString = typeString;
	}
	public String getBagbarcode() {
		return bagbarcode;
	}
	public void setBagbarcode(String bagbarcode) {
		this.bagbarcode = bagbarcode;
	}
	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}
	public String getBagid() {
		return bagid;
	}
	public void setBagid(String bagid) {
		this.bagid = bagid;
	}
}
